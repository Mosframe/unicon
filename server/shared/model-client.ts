// -----------------------------------------------------------------------------
// model-client : 클라이언트 모델
// -----------------------------------------------------------------------------
import {wboolean,wnumber,wstring} from '../shared/default-type';

// -----------------------------------------------------------------------------
/** 데이터 모델 */
// -----------------------------------------------------------------------------
export interface IModel {
    /** 고유 인스턴스 아이디 */
    id : string;
}
// -----------------------------------------------------------------------------
/** 데이터 모델 : 2D 정점 */
// -----------------------------------------------------------------------------
export interface IPoint2 extends IModel {
    /** x축 위치좌표 */
    x : wnumber;
    /** y축 위치좌표 */
    y : wnumber;
}
// -----------------------------------------------------------------------------
/** 데이터 모델 : 직사각형 */
// -----------------------------------------------------------------------------
export interface IRect extends IPoint2 {
    /** 넓이 */
    width   : wnumber;
    /** 높이 */
    height  : wnumber;
}
// -----------------------------------------------------------------------------
/** 데이터 모델 : 독립된 렌더링 객체 */
// -----------------------------------------------------------------------------
export interface IEntity extends IRect {
    /** 맵 아이디 */
    mapId : wstring;
}
// -----------------------------------------------------------------------------
/** 데이터 모델 : 플레이어 */
// -----------------------------------------------------------------------------
export interface IPlayer extends IEntity {
    /** 유저명 */
    userName    : wstring;
    /** 생명력 */
    hp          : wnumber;
    /** 최대 생명력 */
    hpMax       : wnumber;
    /** 점수 */
    score       : wnumber;
}
// -----------------------------------------------------------------------------
/** 데이터 모델 : 테이블 아이템 */
// -----------------------------------------------------------------------------
export interface ITableItem {
    /** 고유 인스턴스 아이디 */
    id      : string;
    /** 이름 */
    name    : string;
}
// -----------------------------------------------------------------------------
/** 데이터 모델 : 캐릭터 인벤토리 슬롯 */
// -----------------------------------------------------------------------------
export interface IInventorySlot {
    /** 테이블 아이템 */
    item    : ITableItem;
    /** 수량 */
    amount  : number;
}
// -----------------------------------------------------------------------------
/** 데이터 모델 : 캐릭터 인벤토리 */
// -----------------------------------------------------------------------------
export interface IInventory {
    /** 인벤토리 슬롯 리스트 */
    slots   : IInventorySlot[];
}
// -----------------------------------------------------------------------------
/** 데이터 모델 : 총알 */
// -----------------------------------------------------------------------------
export interface IBullet extends IEntity {
}
