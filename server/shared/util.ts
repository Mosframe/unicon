import {} from './date';

// -----------------------------------------------------------------------------
/** 박스 인터페이스 */
// -----------------------------------------------------------------------------
interface IRect {
    x       : number,
    y       : number,
    width   : number,
    height  : number,
}

// -----------------------------------------------------------------------------
/** 박스 2개가 서로 충돌했는지 테스트 */
// -----------------------------------------------------------------------------
export const testCollisionRect2 = ( rect1:IRect, rect2:IRect ) : boolean => {
    return rect1.x <= rect2.x+rect2.width
        && rect2.x <= rect1.x+rect1.width
        && rect1.y <= rect2.y+rect2.height
        && rect2.y <= rect1.y+rect1.height;
}
// -----------------------------------------------------------------------------
/** 현재 시간을 로그문자열로 출력 (yyyy-mm-dd hh-mm-ss) */
// -----------------------------------------------------------------------------
export const nowLogString = () : string => {
    var d = new Date();
    return d.toLogString();
}
