/**
 * The various primitives that can be created using the GameObject.CreatePrimitive function.
 *
 * @author mosframe ( https://github.com/mosframe )
 *
 * @enum {number}
 */
export enum PrimitiveType {
    /** A sphere primitive. */
    sphere	    ,
    /** A capsule primitive. */
    capsule     ,
    /** A cylinder primitive. */
    cylinder    ,
    /** A cube primitive. */
    cube        ,
    /** A plane primitive. */
    plane       ,
    /** A Quad primitive. */
    quad        ,
}